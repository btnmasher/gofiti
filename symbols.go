package main

import "strings"

type Symbol struct {
	Pixels [][]byte
	Rune   rune
}

func (s Symbol) Width() int {
	return len(s.Pixels[0])
}

func StringToPixelSymbols(str string) []Symbol {
	var Symbols []Symbol
	for _, s := range strings.ToUpper(str) { //Currently only uppercase
		if l, exists := RuneToSymbol[rune(s)]; exists {
			Symbols = append(Symbols, l)
		} else {
			logger.Fatalf("Did not find matching symbol for given rune: %s", s)
		}
	}
	return Symbols
}

